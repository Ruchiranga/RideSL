function showHide()
{
    if (document.getElementById('cityTaxi').checked)
    {
        document.getElementById('pricingSchemeTaxi').style.display = 'block';
    }
    else
    {
        document.getElementById('pricingSchemeTaxi').style.display = 'none';
    }
};

